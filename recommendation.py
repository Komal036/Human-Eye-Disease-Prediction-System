cnv = """
### Choroidal Neovascularization (CNV)
**What it is:** Abnormal blood vessel growth beneath the retina.

**Symptoms:** Blurred or distorted central vision, dark spots.

**Recommended Actions:**
- Consult a retinal specialist immediately
- Anti-VEGF injections (Ranibizumab, Bevacizumab) are standard treatment
- Regular OCT monitoring every 4–6 weeks
- Avoid smoking — it worsens the condition
"""

dme = """
### Diabetic Macular Edema (DME)
**What it is:** Fluid accumulation in the macula due to diabetes-related damage.

**Symptoms:** Blurred central vision, colours appearing washed out.

**Recommended Actions:**
- Strict blood sugar and blood pressure control
- Anti-VEGF injections or laser photocoagulation
- Regular ophthalmology follow-ups
- HbA1c monitoring every 3 months
"""

drusen = """
### Drusen (Early AMD)
**What it is:** Tiny deposits of lipids/proteins under the retina — early sign of AMD.

**Symptoms:** Often asymptomatic early; may cause mild blurring later.

**Recommended Actions:**
- Annual dilated eye exams
- AREDS2 vitamin supplements (consult doctor)
- Monitor with Amsler grid daily
- Protect eyes from UV light
"""

normal = """
### Normal Retina
**Finding:** No signs of retinal disease detected.

**Recommendations:**
- Continue routine eye exams every 1–2 years
- Maintain a healthy diet rich in leafy greens and omega-3
- Protect eyes from UV exposure
- Report any sudden vision changes immediately
"""
